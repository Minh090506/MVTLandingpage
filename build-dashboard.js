#!/usr/bin/env node
/**
 * MVT Dashboard Builder
 * =====================
 * Reads dashboard.html and bundles it into dashboard-worker.js
 * for deployment to Cloudflare Workers at googlead.myvivatour.com
 *
 * Usage: node build-dashboard.js
 */

const fs = require('fs');
const path = require('path');

const DASHBOARD_HTML = path.join(__dirname, 'dashboard.html');
const OUTPUT_FILE = path.join(__dirname, 'dashboard-worker.js');

function escapeTemplateLiteral(str) {
  return str.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');
}

function build() {
  console.log('🔨 MVT Dashboard Builder');
  console.log('========================\n');

  if (!fs.existsSync(DASHBOARD_HTML)) {
    console.error('❌ dashboard.html not found!');
    process.exit(1);
  }

  const html = fs.readFileSync(DASHBOARD_HTML, 'utf-8');
  console.log(`  ✓ dashboard.html (${(html.length / 1024).toFixed(1)} KB)`);

  const workerCode = `/**
 * MVT Dashboard Worker
 * ====================
 * Serves the Campaign Dashboard at googlead.myvivatour.com
 * Auto-generated by build-dashboard.js — DO NOT EDIT directly.
 * Edit dashboard.html instead, then run: node build-dashboard.js
 *
 * Deploy: npx wrangler deploy -c wrangler-dashboard.toml
 */

const DASHBOARD_HTML = \`${escapeTemplateLiteral(html)}\`;

export default {
  async fetch(request) {
    const url = new URL(request.url);

    // Health check
    if (url.pathname === '/health') {
      return new Response(JSON.stringify({ status: 'ok', service: 'mvt-dashboard', version: '5.0' }), {
        headers: { 'Content-Type': 'application/json' }
      });
    }

    // Serve dashboard for all routes
    return new Response(DASHBOARD_HTML, {
      headers: {
        'Content-Type': 'text/html;charset=UTF-8',
        'Cache-Control': 'public, max-age=300',
        'X-Content-Type-Options': 'nosniff',
        'X-Frame-Options': 'DENY',
      }
    });
  }
};
`;

  fs.writeFileSync(OUTPUT_FILE, workerCode, 'utf-8');
  console.log(`\n✅ Built dashboard-worker.js (${(workerCode.length / 1024).toFixed(1)} KB)`);
  console.log('📦 Deploy: npx wrangler deploy -c wrangler-dashboard.toml');
}

build();
